#ifndef STDTYPES_H_
#define STDTYPES_H_

typedef char int8;
typedef short int int16;
typedef long int int32;
typedef long long int int64;

typedef unsigned char uint8;
typedef unsigned short int uint16;
typedef unsigned long int uint32;
typedef unsigned long long int uint64;

typedef signed char sint8;
typedef signed short int sint16;
typedef signed long int sint32;

typedef volatile unsigned char v_uint8; 
typedef volatile unsigned short int v_uint16;
typedef volatile unsigned long int v_uint32;
typedef volatile unsigned long long int v_uint64;

typedef volatile signed char v_sint8;
typedef volatile signed short int v_sint16;
typedef volatile signed long int v_sint32;

typedef float f32;
typedef double f64;



#endif /* STDTYPES_H_ */